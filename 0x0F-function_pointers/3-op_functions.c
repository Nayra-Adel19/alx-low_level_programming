#include "3-calc.h"


/**
 * op_add - add a + b
 *@a: a (1)
 *@b: b (2)
 *Return: a + b
 */


int op_add(int a, int b)
{
	return (a + b);

}


/**
 * op_sub - difference a - b
 *@a: a (1)
 *@b: b (2)
 *Return: difference a - b
 */


int op_sub(int a, int b)
{
	return (a - b);

}


/**
 * op_mul - multiplies a * b
 *@a: a (1)
 *@b: b (2)
 *Return: product a * b
 */


int op_mul(int a, int b)
{
	return (a * b);

}


/**
 * op_div - divides a / b
 *@a: a (1)
 *@b: b (2)
 *Return: division a / b
 */


int op_div(int a, int b)
{
	return (a / b);

}


/**
 * op_mod - reminder division a % b
 *@a: a (1)
 *@b: b (2)
 *Return: remainder a % b
 */


int op_mod(int a, int b)
{
	return (a % b);

}
